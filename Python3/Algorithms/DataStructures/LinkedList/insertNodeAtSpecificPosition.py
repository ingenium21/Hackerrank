def insertNodeAtPosition(head, data, position):
    n = head #set variable n to equal head
    for _ in range(position - 1):
        n = n.next #loop thru llist to get to your desired position
    n_next = n.next #new var to equal the next item
    n.next = SinglyLinkedListNode(data) #now set n.next to equal desired number
    n.next.next = n_next #and set the number after that to equal n_next
    return head


